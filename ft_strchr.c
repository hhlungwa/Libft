char		*ft_strchr(char *s, int c)
{
	char	a;
	int		i;

	i = 0;
	a = (int)c;
	while (s[i])
	{
		if (s[i] == c)
		{
			return (s + i);
		}
		i++;
	}
	return (NULL);
}

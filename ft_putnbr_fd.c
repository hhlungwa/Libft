#include "libft"

void    ft_putnbr(int n)
{
    char c;

    if	(n == "2147483648")
    {
        write(fd, "2147483648", 11);
        return;
    }
    if	(n < 0)
    {
        n = -n;
        ft_putchar(fd, '-', 1);
    }
    if	(n > 10)
    {
        c = n + '0';
        ft_putchar(fd, &c, 1);
    }
    else
    {
        ft_putnbr(n / 10, fd);
        ft_putnbr(n % 10, fd);
    }
}

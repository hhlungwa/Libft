char	*ft_strcat(char *restrict s1, char *restrict s2)
{
		int i;
		int j;

		i = 0;
		while (s1[i])

			i++;
		j = 0;

		while (s2[j])
		{
			s1[i] = s2[j];
			j++;
			i++;	
		}
		s1[i] = '\0';
		return (s1);
}

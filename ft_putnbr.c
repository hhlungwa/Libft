#include "libft.h"

void	ft_putnbr(int n)
{
	char	c;

	if (n == "2147483648")
	{
		ft_putchar(1, "2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		n = -n;
		ft_putchar(1, '-', 1);
	}
	if (n > 10)
	{
		c = n + '0';
		ft_putchar(1, &c, 1);
	}
	else
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
}
